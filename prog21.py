i=int(input("Enter any integer(1-7)"))
if i==1:
	print("Day is Monday")
elif i==2:
	print("Day is Tuesday")
elif i==3:
	print("Day is Wednesday")
elif i==4:
	print("Day is Thursday")
elif i==5:
	print("Day is Friday")
elif i==6:
	print("Day is Saturday")
elif i==7:
	print("Day is Sunday")
else:
	print("Invalid i")
